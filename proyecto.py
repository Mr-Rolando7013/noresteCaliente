class ProyectoEcologico:

    def __init__(
        self,
        tipo_ecosistema,
        fase_sucesion,
        area_ha,
        objetivos,
        condiciones,
        restricciones
    ):

        self.tipo_ecosistema = tipo_ecosistema
        self.fase_sucesion = fase_sucesion
        self.area_ha = area_ha
        self.objetivos = objetivos
        self.condiciones = condiciones
        self.restricciones = restricciones